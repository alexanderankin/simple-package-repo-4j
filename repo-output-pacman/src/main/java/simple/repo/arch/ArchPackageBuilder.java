    private String render(ArchPkgFilesData.PkgInfoFields pkgInfoFields) {
        var sb = new StringBuilder();
        if (Objects.nonNull(pkgInfoFields.generatedBy))
            sb.append("# Generated by ").append(pkgInfoFields.generatedBy).append("\n");
        sb.append("pkgname = ").append(pkgInfoFields.pkgname).append("\n");
        sb.append("pkgbase = ").append(pkgInfoFields.pkgbase).append("\n");

        // xdata
        sb.append("xdata = pkgtype=").append(pkgInfoFields.xdataPkgType.name());
        if (Objects.nonNull(pkgInfoFields.xdata)) {
            for (var s : pkgInfoFields.xdata.entrySet()) {
                sb.append(",").append(s.getKey()).append("=").append(s.getValue());
            }
        }
        sb.append("\n");

        sb.append("pkgver = ").append(pkgInfoFields.pkgver).append("\n");
        sb.append("pkgdesc = ").append(pkgInfoFields.pkgdesc).append("\n");
        sb.append("url = ").append(pkgInfoFields.url).append("\n");
        sb.append("builddate = ").append(pkgInfoFields.builddate.getEpochSecond()).append("\n");
        sb.append("packager = ").append(pkgInfoFields.packager).append("\n");
        sb.append("size = ").append(pkgInfoFields.size).append("\n");
        sb.append("arch = ").append(pkgInfoFields.arch).append("\n");
        sb.append("license = ").append(pkgInfoFields.license).append("\n");
        if (Objects.nonNull(pkgInfoFields.depend)) {
            for (var s : pkgInfoFields.depend) {
                sb.append("depend = ").append(s).append("\n");
            }
        }

        return sb.toString();
    }

    @SuppressWarnings("OctalInteger")
    private String render(ArchPkgFilesData.MTreeData mTreeData) {
        var sb = new StringBuilder("#mtree");
        for (var line : mTreeData.lines) {
            switch (line) {
                case ArchPkgFilesData.MTreeData.MTreeLine.SetLine setLine -> {
                    sb.append("/set");
                    if (setLine.type != null) sb.append(" type=").append(setLine.type);
                    if (setLine.uid != null) sb.append(" uid=").append(setLine.uid);
                    if (setLine.gid != null) sb.append(" gid=").append(setLine.gid);
                    if (setLine.mode != null)
                        sb.append(" mode=").append(Integer.toString(setLine.mode & 0777, 8)); // only print rwx part
                }
                case ArchPkgFilesData.MTreeData.MTreeLine.FileLine fileLine -> {
                    sb.append(".").append(fileLine.name);
                    if (fileLine.time != null) sb.append(" time=").append(fileLine.time.getEpochSecond()).append(".0");
                    if (fileLine.mode != null)
                        sb.append(" mode=").append(Integer.toString(fileLine.mode & 0777, 8)); // only print rwx part
                    if (fileLine.type != null) sb.append(" type=").append(fileLine.type);
                    if (fileLine.size != null) sb.append(" size=").append(fileLine.size);
                    if (fileLine.hashes != null && fileLine.hashes.getSha256() != null)
                        sb.append(" sha256digest=").append(fileLine.hashes.getSha256());
                }
            }
            sb.append("\n");
        }
        return sb.toString();
    }
