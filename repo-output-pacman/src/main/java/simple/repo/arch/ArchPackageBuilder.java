    //<editor-fold desc="compression">
    @SneakyThrows
    private byte[] gzip(String raw) {
        ByteArrayOutputStream out = new ByteArrayOutputStream();
        try (GZIPOutputStream gzip = new GZIPOutputStream(out)) {
            gzip.write(raw.getBytes());
        }
        return out.toByteArray();
    }

    @SneakyThrows
    private byte[] createTarZst(List<FileSpecWithContent> allFiles) {
        ByteArrayOutputStream out = new ByteArrayOutputStream();
        try (TarArchiveOutputStream tarOut = new TarArchiveOutputStream(new ZstdCompressorOutputStream(out))) {
            // tarOut.setLongFileMode(TarArchiveOutputStream.LONGFILE_GNU);

            for (FileSpecWithContent f : allFiles) {

                byte[] content = f.content().getContent();
                TarArchiveEntry entry = new TarArchiveEntry(f.path());
                entry.setSize(content.length);

                if (f.mode() != null) {
                    entry.setMode(f.mode());
                }

                tarOut.putArchiveEntry(entry);
                tarOut.write(content);
                tarOut.closeArchiveEntry();
            }
        }
        return out.toByteArray();
    }
