    record ArchPkgFilesData() {
        /**
         * @param format             Denotes the file format version, represented by a plain positive integer.
         * @param pkgname            The name of the package.
         * @param pkgbase            The base name of a package, usually the same as the pkgname except for split packages.
         * @param pkgver             The version of the package including pkgrel and epoch.
         * @param pkgarch            The architecture of the package.
         * @param pkgbuild_sha256sum The sha256sum in hex format of the PKGBUILD used to build the package.
         * @param packager           The details of the packager that built the package.
         * @param builddate          The build date of the package in epoch.
         * @param builddir           The directory where the package was built.
         * @param startdir           The directory from which makepkg was executed.
         * @param buildtool          The name of the tool ecosystem used to set up the build environment.
         *                           Used for defining a spec for reproducible builds, e.g. the source of the makepkg.conf(5) used.
         * @param buildtoolver       The full version of the buildtool, for example: "$pkgver-$pkgrel-$pkgarch".
         * @param buildenv           (array) The build environment specified in makepkg.conf.
         * @param options            (array) The options set specified when building the package.
         * @param installed          (array) The installed packages at build time including the version information of the package. Formatted as "$pkgname-$pkgver-$pkgrel-$pkgarch".
         * @see <a href=https://man.archlinux.org/man/BUILDINFO.5>man.archlinux.org: man BUILDINFO</a>
         */
        @Builder
        record BuildInfoData(
                int format,
                String pkgname,
                String pkgbase,
                String pkgver,
                String pkgarch,
                String pkgbuild_sha256sum,
                String packager,
                Instant builddate,
                String builddir,
                String startdir,
                String buildtool,
                String buildtoolver,
                List<String> buildenv,
                List<String> options,
                List<String> installed
        ) {
            /**
             * This array contains options that affect the build environment; the defaults are shown here.
             *
             * @see <a href=https://man.archlinux.org/man/makepkg.conf.5.en>man.archlinux.org: man makepkg</a>
             */
            static final List<String> BUILD_ENV_DEFAULTS = List.of("!distcc", "!color", "!ccache", "check", "!sign");

            /**
             * This array contains options that affect default packaging.
             * They are equivalent to options that can be placed in the PKGBUILD;
             * the defaults are shown here.
             *
             * @see <a href=https://man.archlinux.org/man/makepkg.conf.5.en>man.archlinux.org: man makepkg</a>
             */
            static final List<String> OPTIONS_DEFAULTS = List.of("!strip", "docs", "libtool", "staticlibs", "emptydirs");
        }

        @Builder
        record PkgInfoFields(
                String generatedBy,
                String pkgname,
                String pkgbase,
                PkgType xdataPkgType,
                Map<String, String> xdata, // pkgtype=split
                String pkgver, // = 0.33-3
                String pkgdesc, // = Useful set of utilities for interacting with a cloud (cloud images)
                String url, // = https://github.com/canonical/cloud-utils
                Instant builddate, // = 1752758345
                String packager, // = Arch Linux ARM Build System <builder+n1@archlinuxarm.org>
                Integer size, // = 40360
                String arch, // = any
                String license, // = GPL-3.0-only
                List<String> depend // = bash
        ) {
            /**
             * A PKGINFO must contain an xdata keyword assignment
             * that defines a pkgtype assignment in its value (e.g. pkgtype=pkg).
             * <p>
             * The valid pkgtype values are
             * debug (for debug packages),
             * pkg (for single packages),
             * src (for source packages) and
             * split (for split packages).
             *
             * @see <a href=https://man.archlinux.org/man/PKGINFO.5.en>man.archlinux.org: man PKGINFO</a>
             */
            enum PkgType {
                debug, pkg, src, split
            }
        }

        record MTreeData(List<MTreeLine> lines) {
            sealed interface MTreeLine {
                // https://man.archlinux.org/man/ALPM-MTREE.5.en
                // In contrast to all available types in mtree, ALPM-MTREE only allows three file types:
                enum Type { dir, file, link }

                record SetLine(Type type, Integer uid, Integer gid, Integer mode) implements MTreeLine {
                }

                record FileLine(String name, Instant time, Integer mode, Type type, Integer size,
                                FileIntegrity hashes) implements MTreeLine {
                }
            }
        }
    }

